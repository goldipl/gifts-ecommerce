<script>
  function increase() {
    let input = document.getElementById('price-value');
    input.value = Math.max(0, parseInt(input.value) + 1);
  }

  function decrease() {
    let input = document.getElementById('price-value');
    input.value = Math.max(0, parseInt(input.value) - 1);
  }

  function toggleDropdown() {
    document.querySelector('.branding-custom-select').classList.toggle('open');
  }
  document.addEventListener('click', function(event) {
    let dropdown = document.querySelector('.branding-custom-select');
    if (!dropdown.contains(event.target)) {
      dropdown.classList.remove('open');
    }
  });

  function changeValue(id, delta) {
    let input = document.getElementById(id);
    let newValue = parseInt(input.value) + delta;
    if (newValue >= 0) { // Prevent negative values
      input.value = newValue;
    }
  }
</script>